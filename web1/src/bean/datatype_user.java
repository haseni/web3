package bean;

public class datatype_user {
	private String user_id;
	private String user_account;
	private String user_name;
	private String user_password;
	private String user_email;
	private String user_mobile;
	private String user_qq;
	private String user_privilege;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String userId) {
		user_id = userId;
	}
	public String getUser_account() {
		return user_account;
	}
	public void setUser_account(String userAccount) {
		user_account = userAccount;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String userName) {
		user_name = userName;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String userPassword) {
		user_password = userPassword;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String userEmail) {
		user_email = userEmail;
	}
	public String getUser_mobile() {
		return user_mobile;
	}
	public void setUser_mobile(String userMobile) {
		user_mobile = userMobile;
	}
	public String getUser_qq() {
		return user_qq;
	}
	public void setUser_qq(String userQq) {
		user_qq = userQq;
	}
	public String getUser_privilege() {
		return user_privilege;
	}
	public void setUser_privilege(String userPrivilege) {
		user_privilege = userPrivilege;
	}

}
